public class IntegerMaxExample1 {  
    public static void main(String[] args) {  
        // get two integer numbers  
          int x = 5485;  
          int y = 3242;     
          // print the larger number between x and y  
          System.out.println("Math.max(" + x + "," + y + ")=" + Math.max(x, y));  
    }  
}  