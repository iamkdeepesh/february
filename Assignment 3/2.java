public class SumOfNaturalNumber
{  
public static void main(String[] args)   
{  
int num = 100, i = 1, sum = 0;   
while(i <= num)  
{  
//adding the value of i into sum variable  
sum = sum + i;   
i++;  
}  
System.out.println("Sum of First 100 Natural Numbers is = " + sum);  
}  
}  